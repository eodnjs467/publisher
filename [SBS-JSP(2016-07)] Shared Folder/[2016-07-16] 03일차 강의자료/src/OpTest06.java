public class OpTest06 {
	public static void main(String[] args) {

		int num1 = 10;
		int num2;
		
		num2 = num1 + 10;
		System.out.println(num2); // num2ดย 20
		
		num2 += 10; // num2 = num2 + 10;
		System.out.println(num2);
		
		num2 -= 10; // num2 = num2 - 10;
		System.out.println(num2);
		
		
		
	}
}
