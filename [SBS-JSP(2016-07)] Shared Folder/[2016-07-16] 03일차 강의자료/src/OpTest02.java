
public class OpTest02 {

	public static void main(String[] args) {
		
		int num = 10;
		
		// num++;  =>   num = num+1;
		System.out.println( num++ );
		System.out.println( num );
		System.out.println( ++num );
		System.out.println( num );
		
		
		num++;
		System.out.println(num);
		
		boolean myBool;
		myBool = false;
		System.out.println(myBool);
		System.out.println(!myBool);
	}

}
