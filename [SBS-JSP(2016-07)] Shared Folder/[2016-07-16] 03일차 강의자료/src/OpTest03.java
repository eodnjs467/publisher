
public class OpTest03 {

	public static void main(String[] args) {
		
		int num1 = 10;
		int num2 = 3;
		
		System.out.println(num1 >  num2); // 초과 true 
		System.out.println(num1 >= num2); // 이상 true
		System.out.println(num1 <  num2); // 미만 false
		System.out.println(num1 <= num2); // 이하 false
		System.out.println(num1 == num2); // 동일 false
		System.out.println(num1 != num2); // 다름 true 
	}
}
