public class ForTest01 {
	public static void main(String[] args) {
		
		// (초기화변수	; 조건식		; 증감식)
		for(int i=1 	; i <= 5 	; i++	){
			// i=1, 1<=5:true, 출력, i++
			// i=2, 2<=5:true, 출력, i++
			// i=3, 3<=5:true, 출력, i++
			// i=4, 4<=5:true, 출력, i++
			// i=5, 5<=5:true, 출력, i++
			// i=6, 6<=5:false 반복문 종료
			System.out.println("이 문장을 5회 출력됩니다!");
		}

		// 숙제 for 문의 다른 형식을 찾아보자
	}
}
